#################################################################################
# new
#--------------------------------------------------------------------------------
# construtor
#################################################################################
sub new {
   my $proto = shift;
   my $class = ref($proto) || $proto;
   my $self =  {
      qtdeQuestoes => undef,
   };
   bless ($self,$class);
   $self->clear;
   return $self;
}
#################################################################################









#################################################################################
# clear
#--------------------------------------------------------------------------------
# método para atributos receberem valores default
#################################################################################
sub clear {
   my $self = shift;
   $self->{qtdeQuestoes} = 0;
}
#################################################################################









#################################################################################
# setQtdeQuestoes e getQtdeQuestoes
#--------------------------------------------------------------------------------
# métodos para manipular o atributo qtdeQuestoes
#################################################################################
sub setQtdeQuestoes {
   my $self = shift;
   if (@_) {
      $self->{qtdeQuestoes} = $_[0];
   } else {
      die "Quantidade de questões no teste inválida!\n";
   }
}
#--------------------------------------------------------------------------------
sub getQtdeQuestoes {
   my $self = shift;
   return $self->{qtdeQuestoes};
}
#################################################################################










#################################################################################
# getDados
#--------------------------------------------------------------------------------
# métodos que apenas lista as informações de cada atributo
#################################################################################
sub getDados {
   my $self = shift;
   print "qtdeQuestoes = ".$self->getQtdeQuestoes()."\n";
}
#################################################################################












1;
